export function buildHallOfFameSearchText(entry) {
  return [
    entry.title,
    entry.teamName,
    entry.contestName,
    entry.award,
    entry.summary,
    entry.problem,
    entry.solution,
    entry.impact,
    entry.whyItWon,
    ...(entry.tags || []),
    ...(entry.takeaways || []),
    ...((entry.structure || []).map((segment) => `${segment.label} ${segment.title} ${segment.description} ${segment.objective || ''}`)),
  ]
    .filter(Boolean)
    .join(' ');
}

export async function seedHallOfFame() {
  const collection = getCollection('hall_of_fame');
  await collection.createIndex({ slug: 1 }, { unique: true });
  await collection.createIndex({ isPublic: 1, featured: -1, year: -1 });
  await collection.createIndex({ field: 1, year: -1 });

  const operations = hallOfFameEntries.map((entry, index) => {
    const resourceTypes = new Set((entry.resources || []).map((resource) => resource.type));
    const createdAt = new Date(`${entry.year}-09-${String(10 + index).padStart(2, '0')}T08:00:00.000Z`);

    return collection.updateOne(
      { slug: entry.slug },
      {
        $set: {
          ...entry,
          searchText: buildHallOfFameSearchText(entry),
          hasProject: entry.hasProject || resourceTypes.has('project'),
          hasSlides: entry.hasSlides || resourceTypes.has('slides'),
          hasVideo: entry.hasVideo || resourceTypes.has('video'),
          updatedAt: new Date(),
        },
        $setOnInsert: {
          createdAt,
        },
      },
      { upsert: true },
    );
  });

  await Promise.all(operations);
  console.log(`✅ Seeded ${hallOfFameEntries.length} Hall of Fame entries`);
}
