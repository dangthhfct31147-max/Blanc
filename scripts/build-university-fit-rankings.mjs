import fs from 'node:fs/promises';
import path from 'node:path';
import process from 'node:process';

const THE_WORLD_RANKING_URL = 'https://www.timeshighereducation.com/world-university-rankings/latest/world-ranking';
const WEBOMETRICS_VIETNAM_URL = 'https://www.webometrics.org/vietnam';
const OUTPUT_PATH = path.join(process.cwd(), 'components', 'university-fit', 'generatedRankings.ts');

async function fetchText(url) {
  const response = await fetch(url, {
    headers: {
      'user-agent': 'Mozilla/5.0 (compatible; ContestHubBot/1.0; +https://contests.example)',
      accept: 'text/html,application/xhtml+xml',
    },
  });

  if (!response.ok) {
    throw new Error(`Failed to fetch ${url}: ${response.status}`);
  }

  return response.text();
}

function parseTheWorldTop100(html) {
  const nextDataMatch = html.match(/<script id="__NEXT_DATA__" type="application\/json">([\s\S]*?)<\/script>/);

  if (!nextDataMatch) {
    throw new Error('Unable to locate THE __NEXT_DATA__ payload.');
  }

  const nextData = JSON.parse(nextDataMatch[1]);
  const rows = nextData?.props?.pageProps?.page?.rankingsTableConfig?.rankingsData?.data;

  if (!Array.isArray(rows) || rows.length < 100) {
    throw new Error('THE rankings payload did not include enough rows.');
  }

  return rows
    .map((row) => ({
      rank: Number.parseInt(String(row.rank).replace(/[^\d]/g, ''), 10),
      name: String(row.name).trim(),
      country: String(row.location).trim(),
      overallScore: Number.parseFloat(String(row.scores_overall)),
      sourceUrl: new URL(String(row.url), 'https://www.timeshighereducation.com').toString(),
    }))
    .filter((row) => Number.isFinite(row.rank))
    .sort((left, right) => left.rank - right.rank)
    .slice(0, 100);
}

function parseWebometricsVietnamTop100(html) {
  const pattern = /<tr[^>]*>\s*<td[^>]*>\s*(\d+)\s*<\/td>\s*<td[^>]*>\s*(\d+)\s*<\/td>[\s\S]*?<a[^>]*>([^<]+)<\/a>[\s\S]*?<td[^>]*>\s*(\d+)\s*<\/td>\s*<td[^>]*>\s*(\d+)\s*<\/td>\s*<td[^>]*>\s*(\d+)\s*<\/td>/g;
  const rows = [...html.matchAll(pattern)];

  if (rows.length < 100) {
    throw new Error('Webometrics Vietnam page did not include 100 rows.');
  }

  return rows.slice(0, 100).map((match) => ({
    rank: Number.parseInt(match[1], 10),
    worldRank: Number.parseInt(match[2], 10),
    name: match[3].replace(/\s+/g, ' ').trim(),
    country: 'Vietnam',
    impactRank: Number.parseInt(match[4], 10),
    opennessRank: Number.parseInt(match[5], 10),
    excellenceRank: Number.parseInt(match[6], 10),
    sourceUrl: WEBOMETRICS_VIETNAM_URL,
  }));
}

function renderFile(worldTop100, vietnamTop100) {
  const generatedAt = new Date().toISOString();

  return `/* eslint-disable */
// Auto-generated by scripts/build-university-fit-rankings.mjs
// Sources:
// - THE World University Rankings 2026: ${THE_WORLD_RANKING_URL}
// - Webometrics Vietnam Ranking January 2026: ${WEBOMETRICS_VIETNAM_URL}
// Generated at: ${generatedAt}

export interface WorldRankingEntry {
  rank: number;
  name: string;
  country: string;
  overallScore: number;
  sourceUrl: string;
}

export interface VietnamRankingEntry {
  rank: number;
  worldRank: number;
  name: string;
  country: 'Vietnam';
  impactRank: number;
  opennessRank: number;
  excellenceRank: number;
  sourceUrl: string;
}

export const theWorldTop100_2026: WorldRankingEntry[] = ${JSON.stringify(worldTop100, null, 2)};

export const webometricsVietnamTop100_Jan2026: VietnamRankingEntry[] = ${JSON.stringify(vietnamTop100, null, 2)};
`;
}

async function main() {
  const [theHtml, webometricsHtml] = await Promise.all([
    fetchText(THE_WORLD_RANKING_URL),
    fetchText(WEBOMETRICS_VIETNAM_URL),
  ]);

  const worldTop100 = parseTheWorldTop100(theHtml);
  const vietnamTop100 = parseWebometricsVietnamTop100(webometricsHtml);
  const fileContents = renderFile(worldTop100, vietnamTop100);

  await fs.writeFile(OUTPUT_PATH, fileContents, 'utf8');

  console.log(`Wrote ${worldTop100.length} world rows and ${vietnamTop100.length} Vietnam rows to ${OUTPUT_PATH}`);
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
